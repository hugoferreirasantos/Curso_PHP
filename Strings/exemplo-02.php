<?php
// Funções nativas do PHP para Strings:

// Função strtoupper():
$nome = "joao rangel";
$nome = strtoupper($nome); //Função para tornar as letras maiúsculas:
echo $nome;
echo "<br>";

// Função strtolower():
$nome = strtolower($nome); //Função para tornar as letras minúsculas:
echo $nome;
echo "<br>";

// Função ucfirst:
echo ucfirst($nome); //Muda somente a primeira letra da palavra:
echo "<br>";

// Função ucwords():
$nome = ucwords($nome); //Mudar a primeira letra de cada palavra:
echo $nome;
echo "<br>";




?>